public class Switch { 
 public static void main(String[] args) { 
 int number = 5; 
 switch(number) { 
 case 5: 
 System.out.println("Number is 5"); 
 break; 
 case 5: 
 System.out.println("This is another case 5"); 
 break; 
 default: 
 System.out.println("This is the default case"); 
 } 
 } 
} 

/*error: duplicate case label
when there are duplicate cases present in the switch statement the compiler can get
confuse about which statement is to be executed. and it will throw an error.
*/