public class Nine {
    byte b;
    char c;
    short sh;
    int i;
    float f;
    double d;
    long l;
    public static void main(String[] args) {
        Nine n = new Nine();
        System.out.println(n.b);
        System.out.println(n.c);
        System.out.println(n.sh);
        System.out.println(n.i);
        System.out.println(n.f);
        System.out.println(n.d);
        System.out.println(n.l);
        
    }
}
