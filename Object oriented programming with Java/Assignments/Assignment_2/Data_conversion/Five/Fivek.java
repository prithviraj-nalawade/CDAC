public class Fivek {
    public static void main(String[] args) {
        long i = 7;
        System.out.println("binary  :   " + Long.toBinaryString(i));
        System.out.println("octal  :   " + Long.toOctalString(i));
        System.out.println("hexadecimal  :   " + Long.toHexString(i));
    }
}
