public class Fourk {
    public static void main(String[] args) {
        int i = 7;
        System.out.println("binary  :   "+Integer.toBinaryString(i));
        System.out.println("octal  :   "+Integer.toOctalString(i));
        System.out.println("hexadecimal  :   "+Integer.toHexString(i));
    }
}
