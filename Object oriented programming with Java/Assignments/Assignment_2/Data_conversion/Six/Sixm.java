public class Sixm {
    public static void main(String[] args) {
        float f = 12.6f;
        byte b = (byte) f;
        short s = (short)f;
        char c = (char)f;
        long l = (long)f;
        double d = f;
        int i = (int)f;
        System.out.println(b);
        System.out.println(s);
        System.out.println(c);
        System.out.println(l);
        System.out.println(d);
        System.out.println(i);
    }
}
